<h2>Where Is Marketing?</h2>
<p>Online marketing is composed of three main areas, web marketing, email marketing, and social media.</p>
<ul>
    <li>Web marketing includes e-commerce websites, affiliate marketing websites, informational web sites, online advertising (PPC), and organic search engine results.</li>
    <li>Email marketing covers advertising and promotional marketing sent in e-mail to current or potential clients.</li>
    <li>Social media marketing includes advertising and promotional marketing over social networking sites such as Twitter, Facebook, and YouTube.</li>
</ul>
