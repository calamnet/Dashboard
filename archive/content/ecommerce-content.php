<h2>What Is E-commerce?</h2>
<p>Electronic commerce or e-commerce for short, is a type of business transaction initiated over the Internet. The type of business transaction could range from trading goods, auctions, music to services such as social media optimization and business consulting.</p>
<h2>Why Should I Use E-commerce?</h2>
<p>Online and mobile sales are outgrowing retail spending. As computers and smart phones are becoming standard for more families, shopping from the comfort of their home in increasing. An online store has the ability to reach more potential consumers than a brick-and-mortar store front.</p>
<h2>Who Should Use E-commerce?</h2>
<p>You should consider an e-commerce website if you sell trading goods or services and are looking to increase the visibilty of your business.</p>
<h2>When Should I Start Using E-commerce?</h2>
<p>Once you decide on what type of products or services you want to sell, you can launch an e-commerce store within a few weeks. If you already have a supply of products or droppshipper to supply your products you could be selling online in a matter of days.</p>
<h2>Where Is E-commerce?</h2>
<p>E-commerce is global.</p>
