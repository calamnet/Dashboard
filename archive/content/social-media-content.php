<h2>What is Social Media?</h2>
<p>Social Media is a collection of websites and applications that enable users to participate in content-sharing, collaboration, and social networking. Twitter, Facebook, Pinterest, LinkedIN, YouTube, and Instagram are the most popular social media websites.</p>
<h2>Why should I use Social Media?</h2>
<p>Unlike traditional marketing options (television, radio, newspaper, magazine), Social Media content is always available. When you post social media content, it remains available on your network feed. Your followers can also receive a notification on their own feed that you have shared new content with them</p>
<h2>Who should use Social Media?</h2>
<p>Social Media can benefit businesses looking for sales leads, improving brand awareness, or engaging current clients. Depending on the stage of your business and current goals, we can provide the right social media solution.</p>
<h2>When should I start using Social Media?</h2>
<p>The Internet is constantly evolving and whether you are a small start-up, medium-size company, or large-scale corporation, you can use these services as tools to develop brand awareness, build relationships with your audience, and grow your business.</p>
<h2>Where is Social Media?</h2>
<p>Social Media is everywhere. Millions of users utilize these sites on a daily basis, worldwide, at all hours of the day.</p>
