<h2>Corporation (S Corp, C Corp)</h2>
<p>A corporation protects your personal assets and is preferred for its ability to attract outside investors.</p>
<p>Includes:</p>
<ul>
    <li>Filed documents with the state forming your corporation</li>
    <li>Personalized bylaws and resolutions defining who owns and manages the company</li>
    <li>Optional assistance with IRS filings including application for Federal Tax ID and S corp election</li>
</ul>
<h2>Sole Proprietorship</h2>
<p>A sole proprietorship is the simplest form of business. Although not considered a separate legal entity, by default, once you start operating a small business (i.e. selling goods or services) you become a sole proprietorship. In a sole proprietorship, there is no separation between the business and individual owner.</p>
